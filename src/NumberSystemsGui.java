    /* private ButtonHandler class*/
    private class ButtonHandler implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent e) {
           if(e.getSource()==exitJButton){
             JOptionPane.showMessageDialog(null, "Everytime you Exit, The Program terminate!!!\n Thank you");
             System.exit(0);
            } 
           
           else if(e.getSource()==backjButton){
               dispose();
               MainGui gui = new  MainGui();
               gui.setVisible(true);
               gui.setLocationRelativeTo(null);
               
           }
           
           else if(e.getSource()==ResetJButton){
               enterNumberJTextField.setText("");
               BinaryJtextField.setText("");
               DecimalJTextField.setText("");
               HexadecimalJtextField.setText("");
               OctalJTextField.setText("");        
                                    
           }
           else if(e.getSource()==convertjButton){
                /*if condition checking if the consern JtextField is empty*/ 
            if(enterNumberJTextField.getText().length()<=0){
                JOptionPane.showMessageDialog(null, "Don't leave your Unit Empty");
            }// end of if condition checking if the consern JtextField is empty
                       
            else
            {         
            int JTextFieldenterNum = Integer.parseInt(enterNumberJTextField.getText());  
            BinaryJtextField.setText(""+ Integer.toBinaryString(JTextFieldenterNum));
            DecimalJTextField.setText(""+JTextFieldenterNum);
            OctalJTextField.setText(""+Integer.toOctalString(JTextFieldenterNum));
            HexadecimalJtextField.setText(""+ Integer.toHexString(JTextFieldenterNum) );
            
            
            
             }// end of if condition for convertjbutton
          }// end of convertJbutton ActionPerformed  
           
        }// end of actionPerformed 
        
    }//end of private class

