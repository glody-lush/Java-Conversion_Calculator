/* Method that Validate eneterNumberJtextfield*/
    private void enterNumberJTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_enterNumberJTextFieldKeyPressed
        // TODO add your handling code here:
         char c =evt.getKeyChar();
        if(Character.isLetter(c)){
            enterNumberJTextField.setEditable(false);
            
            JOptionPane.showMessageDialog(null, "Please Enter Only Number");
             enterNumberJTextField.setEditable(true);
        }
        else{
            enterNumberJTextField.setEditable(true);
        }
    }//GEN-LAST:event_enterNumberJTextFieldKeyPressed

